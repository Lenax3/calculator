///////////ADDITION///////////
function add(a, b) {
    return a + b;
}
///////////SUBTRACTION///////////
function subtract(a, b) {
    return a - b;
}
///////////MULTIPLICATION///////////
function multiply(a, b) {
    return a * b;
}
///////////DIVISION///////////
function divide(a, b) {
    return a / b;
}
